		<h3 style="color: #FF0000">What is Base Income Salary Agreement?</h3>

Base Salary Investment Agreement is when a person willing to invest their savings, agrees to pay a base salary to a developer for x amount of years in exchange of a small percentage of the company&#39s equity.</br>
Lets assume that an average Joe has a Savings of 120k and is looking for an investment in a tech company. Following the Base Salary Investment Agreement approach the average Joe can agree to pay $500 weekly for 24 months or $2,000 a month for 2 years in exchange of 5% equity instead of taking a big risk and spending 48K from his/her savings as an initial investment.
This approach gives any average Joe the opportunity to invest in any emerging technology company and also gives the developer the flexibility to work on their own project idea. 
A person can invest in multiple projects making agreements that work best for them like for example instead of $500 weekly for 2 years in exchange of 5% in one company, a multiple investment of $250 weekly for 2 years in exchange of 2.5% in two 2 different companies. 

